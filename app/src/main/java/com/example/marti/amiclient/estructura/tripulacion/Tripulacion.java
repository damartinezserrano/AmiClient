public class Tripulacion {

    String consec_movserv;

    String vehiculo;

    String medico;

    String paramedico;

    String paramedico2;

    String enfermera;

    public Tripulacion() {
    }

    public String getConsec_movserv() {
        return consec_movserv;
    }

    public void setConsec_movserv(String consec_movserv) {
        this.consec_movserv = consec_movserv;
    }

    public String getVehiculo() {
        return vehiculo;
    }

    public void setVehiculo(String vehiculo) {
        this.vehiculo = vehiculo;
    }

    public String getMedico() {
        return medico;
    }

    public void setMedico(String medico) {
        this.medico = medico;
    }

    public String getParamedico() {
        return paramedico;
    }

    public void setParamedico(String paramedico) {
        this.paramedico = paramedico;
    }

    public String getParamedico2() {
        return paramedico2;
    }

    public void setParamedico2(String paramedico2) {
        this.paramedico2 = paramedico2;
    }

    public String getEnfermera() {
        return enfermera;
    }

    public void setEnfermera(String enfermera) {
        this.enfermera = enfermera;
    }
}
